const play = (animation: number[][]) => {
	animation.forEach((frame: number[]) => {
		const bits: number[] = [
            255 & frame[0] >> 16,
            255 & frame[0] >> 8,
            255 & frame[0]
        ];
		for (let i = 0; i < frame.length - 1; ++i)
			for (let j = 0; j < 4; ++j)
				bits.push(255 & frame[i + 1] >> 24 - j * 8);
		for (let i = 0; i < bits.length; ++i) {
			const x = i % 17;
			const y = Math.floor(i / 17);
			scrollbit.setPixel(x, y, bits[i]);
		}
        scrollbit.show();
		basic.pause(1000 / 1);
		scrollbit.clear();
	});
}

