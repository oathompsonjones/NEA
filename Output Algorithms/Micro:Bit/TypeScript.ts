const play = (animation: number[][]) => {
    animation.forEach((frame: number[]) => {
        const bits: number[] = [255 & frame[0]];
        for (let i = 0; i < frame.length - 1; ++i)
            for (let j = 0; j < 4; ++j)
                bits.push(255 & frame[i + 1] << j * 8 >> 24);
        for (let i = 0; i < bits.length; ++i) {
            const x = i % 5;
            const y = Math.floor(i / 5);
            led.plotBrightness(x, y, bits[i]);
        }
        basic.pause(1000 / 1);
        basic.clearScreen();
    });
}

